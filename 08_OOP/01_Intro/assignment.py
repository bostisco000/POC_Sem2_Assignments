class RightTriangle:
    def __init__(self, base, height):self.base = base

    
def area(self): 
    return (1/2) * self.base * self.height

triangle_1 = RightTriangle(3, 4)
print("The area of triange_1 is", triangle_1.area())#Do the same thing for another triangle.
triangle_2 = RightTriangle(6, 8)
print("The area of triangle_2 is", triangle_2.area())